package io.tradeledger.searchfacade.model;

public class Range {
    private String from;
    private String to;

    public Range(){

    }

    public String getFrom() {
        return from;
    }

    public void setFrom(String from) {
        this.from = from;
    }

    public String getTo() {
        return to;
    }

    public void setTo(String to) {
        this.to = to;
    }
}
